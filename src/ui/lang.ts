export type LangKey = keyof typeof LANG; // "en" | "ru" | "uk"

export function t(lang: LangKey, path: string): string {
  const parts = path.split(".");
  let cur: any = LANG[lang] ?? LANG.en;
  for (const p of parts) {
    if (cur && typeof cur === "object" && p in cur) cur = cur[p];
    else return path;
  }
  return String(cur);
}
